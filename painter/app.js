import * as paint from "./js/module/painting.js";
import * as palette from "./js/module/palette.js";
import * as precision from "./js/module/precision.js";
import * as md from "./js/module/modules.js";
import * as tools from "./js/module/tools.js";

function paletteFunctions() {
  palette.createColorOptionInsideColorPalette(obj.colors);
  palette.fillPaletteColorsWithItsRespectiveColor(obj.colors);
  palette.getPaletteColorOnClick();
}

function precisionFuncitons() {
  precision.validatePrecisionValue();
  precision.setContainerGridTemplateColumns(obj.precision);
  precision.fillContainerWithPixelBoxesBasedOnThePrecisionVariable(
    obj.precision
  );
  precision.setNumberOfPixelBoxesOnPrecisionValueChange(obj.precision);
  precision.preventTypingInsidePrecisionInputField();
  precision.myFunc();
}

function paintFunctions() {
  paint.setCurrentColorToBlackOnPageLoad();
  paint.paintPixelsOnLoad();
  paint.getColorFromColorPicker();
}

function moduleFunctions() {
  md.addRemoveGrid();
  md.clearCanvas();
}

function toolFunctions() {
  tools.setCurrentlyUsedTool();
}

function playingNotPossible() {
  if (window.innerWidth < 1200) {
    document.body.innerHTML = `
    <div id="not-available">
      <h2>Notice! <br> This app is a demo of JavaScript code and does not focus on CSS. To use it, please, open it on a pc or mac.</h2>
    </div>`;
    return;
  }
}

window.onload = () => {
  if (window.innerWidth < 1200) {
    playingNotPossible();
    return;
  }
  paletteFunctions();
  precisionFuncitons();
  paintFunctions();
  moduleFunctions();
  toolFunctions();
};

// set precision on precision value change
(() => {
  let precValue = document.querySelector("#precision");

  precValue.addEventListener("change", () => {
    paintFunctions();
  });
})();
